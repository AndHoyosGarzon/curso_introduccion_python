### Operadores Aritmeticos ###

print(3 + 4) # suma 7
print(3 - 4) # resta -1
print(3 * 4) # multiplica 12
print(3 / 4) # divide 0.75
print(3 % 4) # modulo resto 3
print(10 // 3) # division con redonde a un entero 3
print(2 ** 3) # operacion con exponente 8
print("Hola " + "Python") # Solo funciona con addicion pero no con otros operadores "hola python"
print("Hola "+ str(8)) # Funciona con numero transformados a tipo de dato string 
print("Hola " * 5) # Se imprime hola 5 veces ya que le estamos diciendo al lenguaje que nos imprima la palabra 5 veces

